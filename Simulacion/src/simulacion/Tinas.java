    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jTable2.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "# de corrida", "Tina", "# aleatorio", "Peso simulado", "Peso simulado acumulado", "多Se excede la capacidad del camion?"
            }
        ));        
        int k=0;
        try {
            k=Integer.parseInt(cant.getText());
        } catch (Exception e) {
        }
        DefaultTableModel modelo =(DefaultTableModel) jTable2.getModel();        
        Object[] fila = new Object[6];
        int x=0;
        int cont=0;
        double ran=0;
        for (int i = 0; i < k; i++) {
            fila[0]=i+1;
            int pa=0;
            for (int j = 0; j < 5; j++) {                
                fila[1]=j+1;
                ran=Math.random();
                fila[2]=ran;
                if (ran<0.5){
                    x=(int)(190+Math.pow((800*ran),0.5));                                                                                
                }else{                    
                    x=(int)(230-(Math.pow((800*(1-ran)),0.5)));                   
                }                                                                                                                                                                                                                            
                fila[3]=x;
                pa=pa+x;
                fila[4]=pa;
                if (j==4) {
                    if (pa>1000) {
                        fila[5]="Si";
                        cont++;
                        }else
                            fila[5]="No";  
                }
                modelo.addRow(fila);  
                fila[5]=" ";
            }
                                 
        }              
        double tot=(double)cont/k;               
        JOptionPane.showMessageDialog( null,"La proobabilidad de que se exceda la capacidad del camion es: "+tot  );        
        jTable2.setModel(modelo);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

